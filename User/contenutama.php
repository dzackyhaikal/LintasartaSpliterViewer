<p>Lintasarta (PT Aplikanusa Lintasarta) adalah perusahaan Indonesia yang bergerak di bidang penyedia jasa komunikasi data, internet & IT Services.</p>

<p>Sejak didirikan pada bulan April 1988, Lintasarta telah menjadi bagian dari dinamika bisnis di Indonesia dengan menghadirkan layanan komunikasi data, informasi bisnis serta internet yang handal hingga ke wilayah-wilayah terpencil di Nusantara.</p>

<p>Lintasarta telah dipercaya untuk melayani lebih dari 1.700 mitra perusahaan, dan telah membangun koneksi ke lebih dari 20.000 jaringan, dengan memanfaatkan infrastruktur canggih berbasiskan Platform Next Generation Network (NGN). Layanan Lintasarta didukung oleh local support di lebih dari 44 kota di berbagai penjuru di Indonesia.</p>

<p>Strategi Aliansi</p>

